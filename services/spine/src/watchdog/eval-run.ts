// Evaluates Spine 3 on labelled synthetic replicates and writes the verdict
// where serving code can read it:
//
//   services/spine/metrics/watchdog-metrics.json   the machine readable verdict
//   services/spine/metrics/WATCHDOG-METRICS.md     the human readable table
//
// Fully offline: the synthetic generator is seeded, so this needs no
// database and always reproduces the committed numbers. Usage:
//
//   pnpm watchdog:eval

import { mkdirSync, writeFileSync } from "node:fs";
import { dirname, join } from "node:path";
import { fileURLToPath } from "node:url";
import { DEFAULT_WATCHDOG_CONFIG } from "./config.ts";
import { evaluateDetectors, buildWatchdogMetrics, type WatchdogMetrics } from "./eval.ts";

const spineRoot = join(dirname(fileURLToPath(import.meta.url)), "..", "..");
const METRICS_DIR = join(spineRoot, "metrics");

function pct(x: number | null): string {
  return x === null ? "never fired" : `${(x * 100).toFixed(1)}%`;
}

function detectorRow(name: string, d: WatchdogMetrics["baseline"]): string {
  return (
    `| ${name} | ${pct(d.precision)} | ${pct(d.recall)} | ${d.f1} | ` +
    `${d.truePositives} | ${d.falsePositives} | ${d.falseNegatives} |`
  );
}

function verdictSentence(m: WatchdogMetrics): string {
  if (m.verdict === "promoted") {
    return (
      `The forest beat the fixed threshold on F1 over ${m.positives} labelled leak days, ` +
      `so forest:v1 serves. The threshold never fired because the injected leakage hides ` +
      `inside one kombi's takings: a heavy skim on one of four vehicles moves the route ` +
      `total by well under the 40 percent drop the threshold needs. That dilution is the ` +
      `real world case, and it is exactly the argument for a model over a rule here.`
    );
  }
  return (
    `The forest did not beat the fixed threshold on held out labelled days, so the ` +
    `threshold keeps serving. A model earns its place with evidence or not at all.`
  );
}

function renderMarkdown(m: WatchdogMetrics): string {
  return `# Spine 3 revenue watchdog: evaluation

Generated by \`pnpm watchdog:eval\` at ${m.generatedAt}. Method: ${m.replicates}
independent synthetic replicates, each ${m.trainDaysPerReplicate} training days
and ${m.testDaysPerReplicate} held out days. Both detectors are fit on the
training window only and scored on the held out tail against the generator's
own ground truth labels. Data source: ${m.dataSource} (the generator and its
config are committed at \`src/watchdog/simulate.ts\` and
\`src/watchdog/config.ts\`; see the dataset statement for what is real and
what is not).

Held out days: ${m.replicates * m.testDaysPerReplicate}, of which ${m.positives}
carry injected leakage.

| Detector | Precision | Recall | F1 | TP | FP | FN |
| --- | --- | --- | --- | --- | --- | --- |
${detectorRow("Fixed threshold (the named baseline, 60% drop rule)", m.baseline)}
${detectorRow("Isolation forest over per route per day features", m.forest)}

Verdict: **${m.verdict}**. Served engine: **${m.served}**.

## The promotion rule

The forest serves only while it beats the fixed threshold on F1 over held out
labelled days. The rule lives in \`src/watchdog/eval.ts\` and serving code
reads only the verdict in \`watchdog-metrics.json\`; nothing else decides.

## Verdict

${verdictSentence(m)}

Flags always describe patterns (a day, a route, an unnamed vehicle), never a
person. That rule is enforced by unit test on the explanation templates.
`;
}

const report = evaluateDetectors(DEFAULT_WATCHDOG_CONFIG);
const metrics = buildWatchdogMetrics(report, { generatedAt: new Date().toISOString() });

mkdirSync(METRICS_DIR, { recursive: true });
writeFileSync(
  join(METRICS_DIR, "watchdog-metrics.json"),
  JSON.stringify(metrics, null, 2) + "\n",
);
writeFileSync(join(METRICS_DIR, "WATCHDOG-METRICS.md"), renderMarkdown(metrics));

console.log(
  `watchdog eval: ${metrics.positives} leak days held out | ` +
    `baseline F1 ${metrics.baseline.f1} | forest F1 ${metrics.forest.f1} | ` +
    `verdict ${metrics.verdict}, serving ${metrics.served}`,
);
console.log("wrote metrics/watchdog-metrics.json and metrics/WATCHDOG-METRICS.md");
